import Foundation

// Example 1: Arithmetic Operators
let a = 10, b = 5
let addition = a + b, subtraction = a - b, multiplication = a * b, division = a / b, remainder = a % b

// Example 2: Comparison Operators
let x = 10, y = 20
let isEqual = x == y, isGreaterThan = x > y, isLessThan = x < y

// Example 3: Logical Operators
let isTrue = true, isFalse = false
let andResult = isTrue && isFalse, orResult = isTrue || isFalse, notResult = !isTrue

// Example 4: Assignment Operators
var total = 10
total += 5; total -= 3; total *= 2; total /= 4

// Example 5: Ternary Conditional Operator
let condition = true
let result = condition ? "It's true" : "It's false"
